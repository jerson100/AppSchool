package models;

public class Aul_DescNotas {
	
	private int idNotas;
	private String codNotas;
	private String descNotas;
	private int flagEstado;
	
	public int getIdNotas() {
		return idNotas;
	}
	public void setIdNotas(int idNotas) {
		this.idNotas = idNotas;
	}
	public String getCodNotas() {
		return codNotas;
	}
	public void setCodNotas(String codNotas) {
		this.codNotas = codNotas;
	}
	public String getDescNotas() {
		return descNotas;
	}
	public void setDescNotas(String descNotas) {
		this.descNotas = descNotas;
	}
	public int getFlagEstado() {
		return flagEstado;
	}
	public void setFlagEstado(int flagEstado) {
		this.flagEstado = flagEstado;
	}
	
	
	
}
