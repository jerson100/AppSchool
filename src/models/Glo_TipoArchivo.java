package models;

public class Glo_TipoArchivo {

	private int idTipoArchivo;
	private String extension;
	private String icono;
	private long tamagnoMax;
	
	public int getIdTipoArchivo() {
		return idTipoArchivo;
	}
	public void setIdTipoArchivo(int idTipoArchivo) {
		this.idTipoArchivo = idTipoArchivo;
	}
	public String getExtension() {
		return extension;
	}
	public void setExtension(String extension) {
		this.extension = extension;
	}
	public String getIcono() {
		return icono;
	}
	public void setIcono(String icono) {
		this.icono = icono;
	}
	public long getTamagnoMax() {
		return tamagnoMax;
	}
	public void setTamagnoMax(long tamagnoMax) {
		this.tamagnoMax = tamagnoMax;
	}
	
	
	
}
