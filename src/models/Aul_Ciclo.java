package models;

public class Aul_Ciclo {
	
	private int idCiclo;
	private String descCiclo;
	private int flagEstado;
	
	public int getIdCiclo() {
		return idCiclo;
	}
	
	public void setIdCiclo(int idCiclo) {
		this.idCiclo = idCiclo;
	}
	
	public String getDescCiclo() {
		return descCiclo;
	}
	
	public void setDescCiclo(String descCiclo) {
		this.descCiclo = descCiclo;
	}
	public int isFlagEstado() {
		return flagEstado;
	}
	public void setFlagEstado(int flagEstado) {
		this.flagEstado = flagEstado;
	}
	
	
}
